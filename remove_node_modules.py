import shutil
import os

dirs = [ name for name in os.listdir('.') if os.path.isdir(os.path.join('.', name)) ]
folders_deleted = 0

for path in dirs:
    full_path = './'+path+'/node_modules'
    if os.path.isdir(full_path):
        print('Removing folder: ' + full_path)
        shutil.rmtree(full_path)
        folders_deleted += 1

if folders_deleted == 0:
    print('\nWas not possible find any node_modules folders in subdirectories.\n')
else:
    print('\n{0} folder(s) removed.\n'.format(folders_deleted))

input("Press enter to exit")